		<h2 style="text-decoration: underline;"> HISTORY</h2>
		<p><pre>Skittles were first made commercially in 1974 by a British company. An animated television advertisement from that year bears the logo of the "Galaxy" company and is copyrighted by Jack Candies Ltd, a Mars distribution subsidiary. They were first introduced in North America in 1979 as an import confectionery. In 1982, domestic production of Skittles began in the United States.

The name of the candy, Skittles, comes from the sports game of the same name, named as such for the resemblance of the sweet to items used in the game.

Skittles' "taste the rainbow" theme was created by the New York ad agency D'Arcy Masius Benton & Bowles.</pre></p>
	
