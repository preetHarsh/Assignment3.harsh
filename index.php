	<section id="description">
	<h2 style="text-decoration: underline;"> DESCRIPTION</h2>
	<p><pre>Skittles are produced in a wide variety of flavors and colors, including sour varieties. Skittles has hinted at new flavor releases on its Facebook page, using such statuses as "Locking myself in the Rainbow kitchen until I see some results!" A 2011 posting contained confirmation of a new flavor: "Putting the last touches on a new Skittles flavor. Tweak the Rainbow."

In the United States and Canada, in 2013, Skittles replaced the lime-flavored Skittles with green apple, causing a backlash from many consumers. The lime flavor became part of the Darkside packets, which were discontinued in 2015 and followed up by the Orchards packets, which were discontinued in 2017. Lime was also part of the "Long Lost Lime" packets that came out in summer 2017 and 2018. The Darkside flavor was revived in 2019. In 2021, the "All Lime" packets containing only the lime flavor were released for a limited time. In September 2021, Skittles announced that the green apple-flavored Skittles would be replaced with the original lime flavor. In 2022, Skittles Gummies were released.</pre></p>
